package com.example.crepro;

public class Status {
    int hp;
    int attack;
    int spAttack;
    int spDefence;
    int speed;

    public Status(int hp, int attack, int spAttack, int spDefence, int speed){
        this.hp = hp;
        this.attack = attack;
        this.spAttack = spAttack;
        this.spDefence = spDefence;
        this.speed = speed;
    }
}

